package com.java.control.structures;

public class ForLoopDemo {

	public static void main(String[] args) {
		
		//for loop is a control structure in java which is used to repeat same code execution for given number of times
		
		//++ increment operator // classic for loop
		for (int i = 1; i <= 10; i++) { // i=11; 11 <= 10 ; i = 11
			System.out.println(i);
		}
		
		//decrement operator // classic forloop
		for (int j = 4 ; j > 0; j--) { //  0 > 0; 0
			System.out.println(j);
		}
		
	}

}
